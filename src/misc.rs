#[derive(Clone, Copy)]
pub struct Prng(u64);
impl Prng {
    pub fn new(seed: u64) -> Prng {
        Prng(seed)
    }
    pub fn rand64(&mut self) -> u64 {
        (*self).0 ^= (*self).0 >> 12;
        (*self).0 ^= (*self).0 << 25;
        (*self).0 ^= (*self).0 >> 27;
        u64::wrapping_mul(self.0, 2685821657736338717)
    }
}
